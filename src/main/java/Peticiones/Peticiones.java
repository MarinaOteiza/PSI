package Peticiones;

public class Peticiones {
    protected String codi;
    protected String usuari;
    protected String producte;
    protected String tipus_producte;

    public Peticiones(String codi, String usuari, String producte, String tipus_producte){
        this.codi=codi;
        this.usuari=usuari;
        this.producte=producte;
        this.tipus_producte=tipus_producte;
    }
}
